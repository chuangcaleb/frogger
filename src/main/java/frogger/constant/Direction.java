package frogger.constant;

/**
 *  {@code Direction} is an enumeration of all four cardinal directions, used to communicate the Frog's movement direction.
 */
public enum Direction {
	/** The up direction, negative Y */
	UP,
	/** The down direction, positive Y */
	DOWN,
	/** The left direction, negative X */
	LEFT,
	/** The right direction, positive X */
	RIGHT,
}
